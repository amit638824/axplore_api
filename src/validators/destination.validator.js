/**
 * //Destination Validation Schemas
 * //Joi validation schemas for Destination management endpoints
 */

const Joi = require('joi');
const createDestinationSchema = Joi.array().items(Joi.object({
  leadDestinationId: Joi.string().uuid().optional(), // optional, generated by DB
  leadId: Joi.string().uuid().required().optional(),
  optionNo: Joi.number().integer().required(),
  sequenceNo: Joi.number().integer().required(),
  fromCountryId: Joi.string().uuid().required(),
  fromCityId: Joi.string().uuid().required(),
  toCountryId: Joi.string().uuid().required(),
  toCityId: Joi.string().uuid().required(),
  numberOfTravelers: Joi.number().integer().required(),
  checkInDate: Joi.date().optional().required(),
  isExtendedTrip: Joi.boolean().optional().default(false),
  createdAt: Joi.date().optional(), // usually handled by DB
  createdBy: Joi.string().uuid().optional(),
  modifiedAt: Joi.date().optional(),
  modifiedBy: Joi.string().uuid().optional(),
  checkOutDate: Joi.date().optional().required(),
}));


const updateDestinationSchema = Joi.array().items(Joi.object({
  leadDestinationId: Joi.string().uuid(), // optional, generated by DB
  leadId: Joi.string().uuid().required(),
  optionNo: Joi.number().integer().required(),
  sequenceNo: Joi.number().integer().required(),
  fromCountryId: Joi.string().uuid().required(),
  fromCityId: Joi.string().uuid().required(),
  toCountryId: Joi.string().uuid().required(),
  toCityId: Joi.string().uuid().required(),
  numberOfTravelers: Joi.number().integer().required(),
  checkInDate: Joi.date().optional().required(),
  isExtendedTrip: Joi.boolean().optional().default(false),
  modifiedAt: Joi.date().optional(),
  modifiedBy: Joi.string().uuid().required(),
  checkOutDate: Joi.date().optional().required(),
}));



const createServiceSchema = Joi.array().items(Joi.object({
  leadServiceId: Joi.string().uuid(), // optional, generated by DB
  leadId: Joi.string().uuid().required(),
  serviceCategoryId: Joi.string().uuid().required(),
  serviceTypeId: Joi.string().uuid().required(),
  notes: Joi.string().optional(),
  createdAt: Joi.date().optional(), // usually handled by DB
  createdBy: Joi.string().uuid().optional(),
  modifiedAt: Joi.date().optional(),
  modifiedBy: Joi.string().uuid().optional(),
}));


const updateServiceSchema = Joi.array().items(Joi.object({
  leadServiceId: Joi.string().uuid(), // optional, generated by DB
  leadId: Joi.string().uuid().required(),
  serviceCategoryId: Joi.string().uuid().required(),
  serviceTypeId: Joi.string().uuid().required(),
  notes: Joi.string().optional(),
  modifiedAt: Joi.date().optional(),
  modifiedBy: Joi.string().uuid().optional(),
}));




module.exports = {
  createDestinationSchema,
  updateDestinationSchema,
  createServiceSchema,
  updateServiceSchema,
};
